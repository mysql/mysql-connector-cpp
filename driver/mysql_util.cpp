/* {{{ find_charset */
const OUR_CHARSET * find_charset(unsigned int charsetnr)
{
  const OUR_CHARSET * c = our_charsets60;

  do {
    if (c->nr == charsetnr) {
      return c;
    }
    ++c;
  } while (c[0].nr != 0);
  return NULL;
}
